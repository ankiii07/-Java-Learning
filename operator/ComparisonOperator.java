package operator;

public class ComparisonOperator {

	public static void main(String[] args) {
		
		// comparison operator always return boolean value : true or false
		
		int a = 34;
		int b = 78;
		
		System.out.println(a>b);
		 System.out.println(a<b);
		 System.out.println(a>=b);
		 System.out.println(a<=b);
		 System.out.println(a==b);
		 System.out.println(a!=b);
	}

}
